export function isCategoryEmoji(icon: string): boolean {
  if (!icon || CATEGORY_ICON_MAP[icon]) return false;
  if (CATEGORY_EMOJI_OPTIONS.includes(icon as (typeof CATEGORY_EMOJI_OPTIONS)[number])) return true;
  try {
    return /\p{Extended_Pictographic}/u.test(icon);
  } catch {
    return false;
  }
}

export function resolveCategoryIcon(icon: string): LucideIcon {
  return CATEGORY_ICON_MAP[icon] ?? CircleDollarSign;
}
